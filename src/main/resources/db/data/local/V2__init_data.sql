INSERT INTO product (sku, name, description, price, quantity, created_at, updated_at)
SELECT
    CONCAT('SKU', LPAD(FLOOR(RAND() * 100000), 5, '0')) AS sku,
    CONCAT('Product', id) AS name,
    CONCAT('Description for Product', id) AS description,
    ROUND(RAND() * 1000, 2) AS price,
    FLOOR(RAND() * 100) AS quantity,
    NOW() - INTERVAL FLOOR(RAND() * 365) DAY AS created_at,
    NOW() - INTERVAL FLOOR(RAND() * 365) DAY AS updated_at
FROM
    (SELECT
         (a.N + b.N * 10 + c.N * 100) AS id
     FROM
         (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) AS a
             CROSS JOIN
         (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) AS b
             CROSS JOIN
         (SELECT 0 AS N UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) AS c) AS numbers
LIMIT 100;
